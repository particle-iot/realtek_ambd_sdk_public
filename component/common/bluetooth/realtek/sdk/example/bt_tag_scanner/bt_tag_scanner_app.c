/**
  * @brief Use 16 bit uuid to filter scan information
  * @param[in] uuid 16 bit UUID.
  * @param[in] scan_info point to scan information data.
  * @return filter result
  * @retval true found success
  * @retval false not found
  */
void bt_tag_scanner_handle_check_msg(rpl_mac_t tag_mac, uint32_t tag_scanseq)
{
    int ret;
    bool update = 1;
    uint8_t tag_saveseq[4] = {0};

    ret = rpl_check(tag_mac, tag_scanseq, update);
    if(ret >= 0)
    {
        tag_info_t *tag_info = (tag_info_t *)os_mem_alloc(RAM_TYPE_DATA_ON, sizeof(tag_info_t));
        memset(tag_info, 0, sizeof(tag_info_t));

        if(tag_info == NULL)
        {
            printf("fail to allocate memory\r\n");
        }
        else
        {
            LE_UINT32_TO_ARRAY(tag_saveseq, tag_scanseq);
            for(uint8_t i = 0; i < 6; i++)
            {
                tag_info->addr[i] = tag_mac.addr[5-i];//addr
            }
            for(uint8_t i = 0; i < 4; i++)
            {
                tag_info->seq[i] = tag_saveseq[3-i];//seq
            }

            // after save tag info, then send msg to transfertask
            bt_tag_scanner_send_msg_to_transfertask(tag_info);
        }

    }

}

bool bt_tag_scanner_filter_scan_info_by_uuid(uint16_t uuid, T_LE_SCAN_INFO *scan_info)
{
    uint8_t buffer[32];
    uint8_t pos = 0;

    while (pos < scan_info->data_len)
    {
        /* Length of the AD structure. */
        uint8_t length = scan_info->data[pos++];

        uint8_t type;

        if ((length > 0x01) && ((pos + length) <= 31))
        {
            /* Copy the AD Data to buffer. */
            memcpy(buffer, scan_info->data + pos + 1, length - 1);

            /* AD Type, one octet. */
            type = scan_info->data[pos];

            switch (type)
            {
            case GAP_ADTYPE_16BIT_MORE:
            case GAP_ADTYPE_16BIT_COMPLETE:
            case GAP_ADTYPE_SERVICES_LIST_16BIT:
                {
                    uint16_t *p_uuid = (uint16_t *)(buffer);
                    uint8_t i = length - 1;

                    while (i >= 2)
                    {
                        APP_PRINT_INFO2("  ADV Data: UUID16 List Item %d = 0x%x", i / 2, *p_uuid);
                        if (*p_uuid == uuid)
                        {
                            uint32_t tag_scanseq;
                            rpl_mac_t tag_mac;

                            memcpy(tag_mac.addr, scan_info->bd_addr, 6);
                            BE_BUFFER_TO_UINT32(tag_scanseq, &(scan_info->data[10]));

                            bt_tag_scanner_handle_check_msg(tag_mac, tag_scanseq);
                        }
                        p_uuid++;
                        i -= 2;
                    }
                }
                break;

            default:
                break;
            }
        }

        pos += length;
    }
    return 0;
}


